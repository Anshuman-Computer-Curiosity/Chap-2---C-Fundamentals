#include <iostream>
#include <string>
using namespace std;

int main() {
  // boolean
  bool b = true;
  cout << "bool b = " << b << endl;

  // character
  char c = 'A';
  cout << "char c = " << c << endl;

  // integer
  int i = 123;
  cout << "int i = " << i << endl;

  // floating point
  float f = 3.14f;
  cout << "float f = " << f << endl;

  // double
  double d = 2.718;
  cout << "double d = " << d << endl;

  // long double
  long double ld = 1.23456789;
  cout << "long double ld = " << ld << endl;

  // string
  string s = "Hello, World!";
  cout << "string s = " << s << endl;

  return 0;
}